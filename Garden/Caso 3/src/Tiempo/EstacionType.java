package Tiempo;

public class EstacionType {
	private String estacion;
	private int maxSol;
	private int minSol;
	private int maxRain;
	private int minRain;
	
	public EstacionType(){
		
	}
	
	public String getEstacion() {
		return estacion;
	}

	public void setEstacion(String estacion) {
		this.estacion = estacion;
	}

	public int getMaxSol() {
		return maxSol;
	}

	public void setMaxSol(int maxSol) {
		this.maxSol = maxSol;
	}

	public int getMinSol() {
		return minSol;
	}

	public void setMinSol(int minSol) {
		this.minSol = minSol;
	}

	public int getMaxRain() {
		return maxRain;
	}

	public void setMaxRain(int maxRain) {
		this.maxRain = maxRain;
	}

	public int getMinRain() {
		return minRain;
	}

	public void setMinRain(int minRain) {
		this.minRain = minRain;
	}

	
	
}
